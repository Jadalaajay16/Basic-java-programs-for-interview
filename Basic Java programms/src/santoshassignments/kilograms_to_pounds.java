package santoshassignments;

public class kilograms_to_pounds {

	public static void main(String[] args) {
		System.out.println("Kilograms Pounds");
		System.out.println("------------------------");
		for(int i=1;i<20;i=i+2)
		{
			System.out.println("   "+i +"     "+ String.format("%.1f",i*2.2 ));
		}
		
		
	}

}
